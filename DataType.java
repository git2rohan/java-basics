public class DataType {
    // Datatypes in java are of 2 types - Primitives and Non-Primitives
    // Primitives - int, float, double, char, boolean = These are the container who stores direct value
    // Non-Primitive - String, Array, Integer... etc = These are the objects and stores address of the value

    public static void main(String[] args) {
        int myIntVariable = 5;
        float myFloatVariable = 5.1f;
        double myDoubleVariable = 5.2d;
        char myCharVariable = 'a'; //Single quote and single char
        boolean myBooleanVariable = true; // Can be true of false.

        String myStringVariable = "Rohan"; //THis is an object and stores the value reference
        int[] myIntArray = {1,2,3,4}; //This is an integer array
        String[] myStringArray = {"Rohan","Soudey"}; //This is a string variable

    }
}
